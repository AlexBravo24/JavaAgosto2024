package ciclos;

public class Ciclos1_FIPS {

	public static void main(String[] args) {
		//  Programa un algoritmo que realice la tabla de multiplicar del 12

		 // Usar un bucle for para generar la tabla de multiplicar
        for (int i = 1; i <= 10; i++) {
            int resultado = 12 * i;
            System.out.println("12 x " + i + " = " + resultado);
        }
    }
}