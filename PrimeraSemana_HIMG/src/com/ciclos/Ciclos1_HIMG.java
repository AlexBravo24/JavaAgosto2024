/*
 * 1.- Programa un algoritmo que realice la tabla de multiplicar del 12
 */

package com.ciclos;

public class Ciclos1_HIMG {

	public static void main(String[] args) {
		int num = 12;
		// Inicialización de ciclo for.
		for (int i = 0; i <= 50; i++) {
			System.out.println("12 x " + i + " = " + num * i);
			
		}

	}

}
